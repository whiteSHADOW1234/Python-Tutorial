
friends = 5
friends += 2
print(friends) # 5 + 2 = 7
friends -= 2
print(friends) # 7 - 2 = 5
friends *= 2
print(friends) # 5 * 2 = 10
friends /= 2
print(friends) # 10 / 2 = 5.0
friends %= 2
print(friends) # 5 % 2 = 1.0 --> this gets the remainder
friends **= 2
print(friends) # 1.0 ** 2 = 1.0
friends //= 2
print(friends) # 1.0 // 2 = 0.0 --> this gets the quotient